    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // generate 3bit seq
        String iseq = this.fastaField.getText();
        File f = new File(iseq);
        if(iseq.trim().length()==0 && !(f.exists() && f.isFile())){
            JOptionPane.showMessageDialog(this, "FASTA file is not valid.", "ERROR", JOptionPane.ERROR_MESSAGE);
        }
        else{
            try{
                String oseq = iseq+".3bit";
                    INucleotideStream<B3Nucleotide> fastaStream = new BufferedFASTANucleotideStream(iseq);
                    //while(fastaStream.next()!=null);
                    while(fastaStream.nextCode() != INucleotideStream.NULL_CODE);
                    System.out.println("sequence length " + (fastaStream.position()));
                    B3LLSequence bitgenome = new B3LLSequence(fastaStream.position() + 1);
                    fastaStream.close();
                    fastaStream = new BufferedFASTANucleotideStream(iseq);
                    bitgenome.fillBy(fastaStream);
                    fastaStream.close();
                    System.out.println("done");
                    System.out.println("saving...");
                    bitgenome.save(oseq);
                    System.out.println("done");
                    
                    this.b3seqField.setText(oseq);
            }catch(Exception e){
                    JOptionPane.showMessageDialog(this, "En error occurred during conversion to 3bit.\n"+e.getMessage(), "ERROR", JOptionPane.ERROR_MESSAGE);
                    System.out.println(e);
                    e.printStackTrace();
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // generate nelsa
        String iseq = this.b3seqField.getText();
        File f = new File(iseq);
        if(iseq.trim().length()==0 && !(f.exists() && f.isFile())){
            JOptionPane.showMessageDialog(this, "3bit file is not valid.", "ERROR", JOptionPane.ERROR_MESSAGE);
        }
        else{
            try{
                String o_file = iseq.replace(".3bit", "") + ".nelsa";
                System.out.println("reading 3bit seq...");
                B3LLSequence b3seq = B3LLSequence.load(iseq);
                System.out.println("done");
                System.out.println("building nelsa...");
                NELSA nelsa = new NELSA(b3seq);
                System.out.println("done");
                System.out.println("saving...");
                nelsa.save(o_file);
                System.out.println("done");
                this.nelsaField.setText(o_file);
            }catch(Exception e){
                JOptionPane.showMessageDialog(this, "En error occurred during NELSA generation.\n"+e.getMessage(), "ERROR", JOptionPane.ERROR_MESSAGE);
                System.out.println(e);
		e.printStackTrace();
            }
        }
    }//GEN-LAST:event_jButton2ActionPerformed

