    private void okButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_okButtonActionPerformed
        if(this.nameField.getText().trim().length() != 0){
            answer = true;
            
            seq.setName(this.nameField.getText().trim());
            
            if(this.fastaField.getText().trim().length() == 0){
                seq.setFASTAFile(null);
            }
            else{
                seq.setFASTAFile(this.fastaField.getText().trim());
            }
            
            
            if(this.nelsaField.getText().trim().length() == 0){
                seq.setNELSA(null);
                seq.setNELSAFile(null);
            }
            else{
                if(prevNELSAFile == null  ||   prevNELSAFile.compareTo(this.nelsaField.getText().trim())!=0){
                    seq.setNELSAFile(this.nelsaField.getText().trim());
                    seq.setNELSA(null);
                }
            }
            
            
            if(this.b3seqField.getText().trim().length() == 0){
                seq.setB3seq(null);
                seq.setB3seqFile(null);
            }
            else{
                if(prevB3seqFile == null  ||   prevB3seqFile.compareTo(this.b3seqField.getText().trim())!=0){
                    seq.setB3seqFile(this.b3seqField.getText().trim());
                    seq.setB3seq(null);
                }
            }
            
            this.dispose();
        }
        else{
            this.nameField.setText("insert a valid name");
        }
    }//GEN-LAST:event_okButtonActionPerformed

