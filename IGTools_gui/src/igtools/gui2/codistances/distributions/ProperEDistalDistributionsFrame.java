    private void shuffle(int[] start, int[] end, double p){
    	int nofShuffles = (int)(((double)start.length)*p);
    	int i1, i2, tmp;
    	for(int i=0; i<nofShuffles; i++){
    		i1 = (int)(Math.random()*((double)start.length));
    		i2 = (int)(Math.random()*((double)start.length));
    		
    		tmp = start[i1];
    		start[i1] = start[i2];
    		start[i2] = tmp;
    		
    		tmp = end[i1];
    		end[i1] = end[i2];
    		end[i2] = tmp;
    	}
    }
    
